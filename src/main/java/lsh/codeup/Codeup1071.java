package lsh.codeup;

import java.util.Scanner;

public class Codeup1071 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while(true) {
            int input = sc.nextInt();
            if(input == 0) {
                break;
            } else {
                System.out.println(input);
            }
        }
    }
}
